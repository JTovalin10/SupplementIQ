// Helper function to convert SupplementInfo to IngredientField
function supplementToIngredientField(
  supplement: SupplementInfo,
): IngredientField {
  return {
    name: supplement.name,
    label: supplement.label,
    placeholder: supplement.placeholder,
    unit: supplement.unit,
    description: supplement.description,
    section: supplement.section,
    required: supplement.required || false,
    step: supplement.step,
    type: supplement.type || "number",
  };
}

